//Switch Statement

void main(){
        double x = 5.1;

        //in dart switch is work without break
        switch(x){
                case 1.1:
                        print("mon");
                case 2.1:                                                                                                                       print("tues");
                case 3.1:                                                                                                                       print("wednes");
                case 4.1:                                                                                                                       print("thurs");
                case 5.1:                                                                                                                       print("fri");
                case 6.1:                                                                                                                       print("sat");
                case 7.1:                                                                                                                       print("son");
                default:                                                                                                                       print("wrong inout");
        }
}
